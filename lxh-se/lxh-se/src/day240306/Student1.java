package day240306;

public class Student1 {
    int id;
    String name;
    int age;

    public Student1(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public Student1() {
    }

    public Student1(int id) {
        this.id = id;
    }

    public Student1(int id, String name, int age) {
        this.id = id;
        this.name = name;
        this.age = age;
    }
}
